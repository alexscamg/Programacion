

# Pedimos un numero al usuario
numero = int(input(('Introduce un numero: ')))

# Sentencias
if numero == 0:
    print('Es igual')
elif numero > 0:
    print('El numero es mayor que cero')
else:
    print('El numero es menor que cero')

